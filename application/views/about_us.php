<h1>Acerca de nosotros</h1>

<!-- Descripción -->
<p>TAAC Capacitaciones es una Empresa Socialmente Responsable (ESR). Cuenta con docentes con más de 20 años de experiencia y certificados en diferentes áreas.</p>

<!-- Mision -->
<h2>Misión</h2>
<p>La misión de TAAC Capacitaciones es preparar empresas para el desafio social que implica las nuevas generaciones. 
Prepara a los empleados con las mejores estartegias pedagogicas y lograr que los clientes (Empresas) cuenten con profesionistas capacitados.</p>

<!-- Vision -->
<h2>Visión</h2>
<p>TAAC Capacitaciones se visualiza como la empresa con mayor aporte cultural y profesional en la región de Tijuana/San Diego, 
    ofreciendo cursos interdiciplinarios de la más alta calidad.</p>
